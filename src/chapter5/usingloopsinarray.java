package chapter5;

public class usingloopsinarray {
    public static void main(String[] args) {
        int [] arr=new int[8];
        arr[0]=10;
        arr[1]=15;
        arr[2]=20;
        arr[3]=30;
        arr[4]=50;
        arr[5]=40;
        arr[6]=80;
        arr[7]=70;
        //output->for loop
        for (int i = 0; i <=7; i++) {
            System.out.println(arr[i]+" ");
            
        }

    }
    
}
